import "../styles/globals.scss";
import "bootstrap/dist/css/bootstrap.min.css";
import Head from "next/head";
import Script from "next/script";
import { AuthProvider } from "../context/AuthProvider";
import Layout from "../components/Layout";
import { useEffect, useState } from "react";
import "aos/dist/aos.css";
import AOS from "aos";
import { LoaderContext, useLoader } from "../context/loader";
import { OrderContext } from "../context/orderContext";

function MyApp({ Component, Props }) {
  useEffect(() => {
    AOS.init();
  });
  const [isLoader, setIsLoader] = useState(false);
  const [isOrder, setIsOrder] = useState();
  useEffect(() => {
    setIsLoader(true);
    setTimeout(() => {
      setIsLoader(false);
    }, 2000);
  }, []);
  return (
    <>
      <Head>
        <title>Resto | Home</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/assets/resto.png" />
      </Head>
      <Script
        src="https://kit.fontawesome.com/fe2e019d14.js"
        crossOrigin="anonymous"
      ></Script>
      <OrderContext.Provider value={{ isOrder, setIsOrder }}>
        <LoaderContext.Provider value={{ isLoader, setIsLoader }}>
          <AuthProvider>
            <Layout>
              <Component {...Props} />
            </Layout>
          </AuthProvider>
        </LoaderContext.Provider>
      </OrderContext.Provider>
    </>
  );
}

export default MyApp;
